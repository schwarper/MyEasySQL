namespace MyEasySQL.Model;

/// <summary>
/// Represents the sorting order used in SQL queries.
/// </summary>
public enum OrderType
{
    /// <summary>
    /// Represents ascending order (ASC).
    /// Results will be sorted from the smallest to the largest.
    /// </summary>
    ASC,

    /// <summary>
    /// Represents descending order (DESC).
    /// Results will be sorted from the largest to the smallest.
    /// </summary>
    DESC
}